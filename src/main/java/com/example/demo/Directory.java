package com.example.demo;

public class Directory {
    public int id=-1;
    public String name;
    public String tel;
    public String email;
    public String address;
    public String QQ;

    public Directory(int id, String name, String tel, String email, String address, String QQ) {
        this.id=id;
        this.name=name;
        this.tel=tel;
        this.email=email;
        this.address=address;
        this.QQ = QQ;
    }

}
